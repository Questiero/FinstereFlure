    private void updateDisplayJeton() {

        JLabel[] labelsP1 = {jPion1P1, jPion2P1, jPion3P1, jPion4P1};
        JLabel[] labelsP2 = {jPion1P2, jPion2P2, jPion3P2, jPion4P2};

        for (JLabel label : labelsP1) {
            label.setIcon(new ImageIcon(""));
        }
        for (JLabel label : labelsP2) {
            label.setIcon(new ImageIcon(""));
        }

        for (int i = 0; i < this.game.getPlayer1().getJetons().size(); i++) {
            labelsP1[i].setIcon(new ImageIcon(this.game.getPlayer1().getJetons().get(i).generateSpritePath()));
        }

        for (int i = 0; i < this.game.getPlayer2().getJetons().size(); i++) {
            labelsP2[i].setIcon(new ImageIcon(this.game.getPlayer2().getJetons().get(i).generateSpritePath()));
        }

    }

    private void updateMoveButtons() {
        jUpButton.setEnabled(true);
        jLeftButton.setEnabled(true);
        jRightButton.setEnabled(true);
        jDownButton.setEnabled(true);

        if (!(this.game.getCurrentJeton().isCanPlay())) {
            System.out.println("oui");
            jUpButton.setEnabled(false);
            jLeftButton.setEnabled(false);
            jRightButton.setEnabled(false);
            jDownButton.setEnabled(false);
        } else {

            if (!(this.game.getCurrentJeton().canMove(Direction.UP))) {
                jUpButton.setEnabled(false);
            }
            if (!(this.game.getCurrentJeton().canMove(Direction.LEFT))) {
                jLeftButton.setEnabled(false);
            }
            if (!(this.game.getCurrentJeton().canMove(Direction.RIGHT))) {
                jRightButton.setEnabled(false);
            }
            if (!(this.game.getCurrentJeton().canMove(Direction.DOWN))) {
                jDownButton.setEnabled(false);
            }
        }

    }

